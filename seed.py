def seed():
    app = create_app()
    with app.app_context():
        if Breed.query.count() > 0:
            for breed_data in BREEDS:
                breed = Breed.query.filter_by(name_en=breed_data['name_en']).first()
                if breed:
                    breed.image_url = breed_data['image_url']
            from app import db
            db.session.commit()
            print('Обновлены URL изображений.')
            return

        for breed_data in BREEDS:
            facts_data = breed_data.pop('facts')
            breed = Breed(**breed_data)
            db.session.add(breed)
            db.session.flush()
            for content in facts_data:
                fact = Fact(content=content, breed_id=breed.id)
                db.session.add(fact)

        db.session.commit()
        print(f'Добавлено {len(BREEDS)} пород и {len(BREEDS) * 3} фактов.')


if __name__ == '__main__':
    seed()
